public class CaReview1 {
  public static void main(String[] args) {
    System.out.println("Java is a class-based language.");
    // Java programs have at least one class.
    // Each class has a name that should be capitalized.
    // The name of the class should match the name of the file.
    // Each class represents one concept.
    
    System.out.println("Java classes have a 'main' method.");
    // The main method is where the program starts.
    // The main method is always inside a class.

    System.out.println("Java classes and methods use curly braces to denote blocks of code.");
    // Curly braces mark the scope of a class or method.

    System.out.println("Java statements end with a semicolon.");
    // Semicolons are used to mark the end of a statement.

    System.out.println("Java does not read whitespace but it is useful for making code more readable.");
    // Java does not read whitespace so it is important to use it to make your code more readable.

    System.out.println("Compiling catches errors in your code before your program runs.");
    // Compiling is the process of converting code into a language that the computer understands.
    // Compiling catches errors in your code before your program runs.

    System.out.println("The compiler transforms your java code into an executable class.");
    // The compiler transforms your java code into an executable class.
    // The compiler transforms you java code into an intermediate language called bytecode.
  }
  
}
